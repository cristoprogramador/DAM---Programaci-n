package _03ejercicios;

/**
 * Cuál es el valor resultante de dada una de las siguientes expresiones 
 * a) 5 * 4 – 3 * 6
 * b) 4 * 5 * 2 
 * c) (24 + 2 * 6) / 4 
 * d) 8 / 2 / 2 * 5 
 * e) 3 + 4 * (8 * (4 – (9 + 3) / 6 )) 
 * f) 4 * 3 * 5 + 8 * 4 * 2
 * g) 4 – 40 % 5 
 * h) 4 * 3 / 2 
 * i) 4 / 2 * 3 
 * j) 213 /100
 */
public class _17ValorResultante {

	public static void main(String[] args) {
		System.out.println("5 * 4 – 3 * 6 = " + (5 * 4 - 3 * 6));
		System.out.println("4 * 5 * 2 = " + ( 4 * 5 * 2));
		System.out.println("(24 + 2 * 6) / 4)= " + ((24 + 2 * 6) / 4));
		System.out.println("8 / 2 / 2 * 5 = " + (8 / 2 / 2 * 5));
		System.out.println("3 + 4 * (8 * (4 – (9 + 3) / 6 )) = " + (3 + 4 * (8 * (4 - (9 + 3) / 6 ))));
		System.out.println("4 * 3 * 5 + 8 * 4 * 2 = " + (4 * 3 * 5 + 8 * 4 * 2));
		System.out.println("4 – 40 % 5 = " + (4 - 40 % 5));
		System.out.println("4 * 3 / 2 = " + (4 * 3 / 2));
		System.out.println("4 / 2 * 3 = " + (4 / 2 * 3));
		System.out.println("213 / 100 = " + (213 /100));

	}

}
